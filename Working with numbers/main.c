#include <stdio.h>/**< Working with numbers */
#include <stdlib.h>

int main()
{
    char num = 90;
    printf("%f\n", 5.0 + 4.5); /**<  Math operations with float */
    printf("%f\n", 5 / 4.0); /**<  Math operations with float */
    printf("%d\n", num); /**< integer variable */
    printf("%f\n", pow(2, 3) ); /**< Power operation */
    printf("%f\n", sqrt(3) ); /**< Square root operation */
    printf("%f\n", ceil(35.87) ); /**< Round up operation */
    printf("%f\n", floor(35.87) ); /**< Round down operation */

    return 0;
}
